int f[1<<20];
class DistinguishableSetDiv1 {
        public:
        int count(vector<string> an) {
                int n = SZ(an), m = SZ(an[0]);
                CLR(f);
                for(int i = 0; i < n; ++i) {
                        for(int j = 0; j < i; ++j) {
                                int sta = 0;
                                for(int k = 0; k < m; ++k) if(an[i][k] == an[j][k]) sta |= (1<<k);
                                f[sta] = 1;
                        }
                }
                int ans = 0;
                for(int i = (1<<m)-1; i >= 0; --i) {
                        if(f[i]) {
                                for(int j = 0; j < m; ++j) if(i & (1<<j)) f[i^(1<<j)] = 1;
                        } else ++ans;
                }
                return ans;
        }
};

